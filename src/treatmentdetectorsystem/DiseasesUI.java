    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
          if(diseasesTextField.getText().equals(""))
         {
             JOptionPane.showMessageDialog(this, "Please fill ClassRollNo");   
         }
         else if(medicinnameTextField.getText().equals(""))
         {
              JOptionPane.showMessageDialog(this, "Please fill First name");  
         }
         
         else
           {
               
             try {
                 Statement stmt = null;
                  Connection con = DBconnection.ConnecrDb();
                   stmt = con.createStatement();
                 String query ="INSERT INTO addmedicin VALUES('"+diseasesTextField.getText()+"','"+ageComboBox.getSelectedItem().toString()+"','"+medicinnameTextField.getText()+"','"+medicintypeComboBox.getSelectedItem().toString()+"','"+eatingrolllsComboBox.getSelectedItem().toString()+"','"+medicindurationComboBox.getSelectedItem().toString()+"','"+comphanyComboBox.getSelectedItem().toString()+"')";
                      
                 int RowsAffected =  stmt.executeUpdate(query);
                 JOptionPane.showMessageDialog(this,RowsAffected+"New regiration" );
             diseasesTextField.setText("");
           medicinnameTextField.setText("");
          
             } catch (SQLException ex) {
                 Logger.getLogger(DiseasesUI.class.getName()).log(Level.SEVERE, null, ex);
             }
               
          
               
           }
    }//GEN-LAST:event_saveButtonActionPerformed

