    private void SEARCHTextFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_SEARCHTextFieldKeyPressed
        // TODO add your handling code here:
          try{
              Connection con = DBconnection.ConnecrDb();
              String quary = "SELECT * FROM addmedicin WHERE Diseases LIKE '%"+SEARCHTextField.getText()+"'"; 
              
              PreparedStatement ps=con.prepareStatement(quary);
              //pst.setString(1,search_TextField.getText());
              ResultSet rs = ps.executeQuery();
              if(rs.next())
              {
                  String add1=rs.getString("Diseases");
                  diseasesTextField.setText(add1);
                   String add2=rs.getString("Age");
                  ageComboBox.setSelectedItem(add2);
                  String add3=rs.getString("Medicin_Name");
                  medicinnameTextField.setText(add3);
                  String add4=rs.getString("MedicinType");
                  medicintypeComboBox.setSelectedItem(add4);
                  String add5=rs.getString("EatingRols");
                  eatingrolllsComboBox.setSelectedItem(add5);
                  String add6=rs.getString("MedicinDuration");
                  medicindurationComboBox.setSelectedItem(add6);
                  String add7=rs.getString("Comphany");
                  comphanyComboBox.setSelectedItem(add7);
                   
                     
                  
              }
          }catch(Exception e)
          {
                     JOptionPane.showMessageDialog(this, e );
          }
        
    }//GEN-LAST:event_SEARCHTextFieldKeyPressed

